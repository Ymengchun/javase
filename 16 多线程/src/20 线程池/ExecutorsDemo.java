package Thread_08;

import java.util.concurrent.ExecutorService;
import java.util.concurrent.Executors;

/*
 * 为什么要引入线程池？
 * 	启动新线程的成本高（新线程要与OS交互）
 * 	而线程池中的线程在运行结束后，不会死亡，而是再次回到线程池中成为空闲状态，等待下一个对象调用。
 * 
 * 实现线程池
 * 		1.创建线程池的方法有好多种，我们选用如下方法。
 * 			创建一个线程池对象，并说明要创建的线程对象的个数：public static ExecutorService newFixedThreadPool(int nThreads)
 * 		2.用这种方法创建的线程池中的线程可以执行：
 * 			Runnable对象或Callable对象代表的线程
 * 			创建一个类，实现Runnable接口。
 * 		3.调用如下方法即可
 * 			Future<?> submit(Runnable task)
 *			<T> Future<T> submit(Callable<T> task)
 *		4.shutdown()才结束，否则返回线程池。
 */
public class ExecutorsDemo {
	public static void main(String[] args) {
		//创建线程池对象，说明要创建2个线程对象。
		ExecutorService pool = Executors.newFixedThreadPool(2);

		//可以执行Runnable对象或Callable对象代表的线程
		pool.submit(new MyRunnable());
		pool.submit(new MyRunnable());

		//结束线程池（没有这一步，线程返回线程池不会结束）
		pool.shutdown();
	}
}
